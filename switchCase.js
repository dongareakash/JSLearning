var dayofWeek = function (day) {
    switch (day) {
        case 1:
            console.log(`${day} - monday`);
            break;

            case 2:
                console.log(`${day} - tuesday`);
                break;

                case 3:
                    console.log(`${day} - wenseday`);
                    break;
    
        default:
            break;
    }
}
dayofWeek(2);
dayofWeek(7);